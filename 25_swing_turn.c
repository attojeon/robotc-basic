int turn90ByDegrees(float diameter, float length) //diameter = 53, length = 120
{
	return (((2 * 3.14 * length)/4) / (3.14 * diameter)) * 360.0;
}

task main()
{
	float dist_angle;
	dist_angle = turn90ByDegrees(53.0, 120.0);
	displayBigTextLine(5, "%f",dist_angle);
	sleep(2000);

	resetMotorEncoder(motorC);

	while (getMotorEncoder(motorC) < dist_angle)
	{
		setMotorSpeed(motorB, 0);
		setMotorSpeed(motorC, 10);
	}

	setMotorSpeed(motorB, 0);
	setMotorSpeed(motorC, 0);
}
